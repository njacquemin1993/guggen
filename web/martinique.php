  <h1>Les Mokshû Lion's au Carnaval de Martinique (bis)</h1>

  <p>Lors du retour de notre 1er voyage en Martinique en 2001, notre président de l'époque, Christian Baudoin, nous avait fait une promesse : on y reviendra un jour...</p>
  <img src="images/groupeplage.jpg" />
  <p>
    Six ans plus tard, cette promesse a été tenue ! 32 personnes ont donc traversé l'Atlantique du 10 au 22 février 2007 afin de vivre une aventure qu'elles ne sont pas prêtes d'oublier.
    Voici un petit résumé de ce que nous avons vécu là-bas... bien que cela soit vraiment impossible à expliquer.
  </p>

  <h2>Le départ </h2>
  <p>
    Samedi matin 04h30, le car Torgon Excursions nous emmène jusqu'à l'aéroport d'Annecy, petit vol sans histoire jusqu'à Paris pour prendre un B777 flambant neuf et arrivée avec un peu de retard à Fort-de-France.
    Nos amis Martiniquais ainsi que les 4 Mokshû qui étaient partis en éclaireurs réceptionner les caisses remplies d'instruments sont également en retard...
    ça commence bien, mais c'est pour nous mettre tout de suite dans l'ambiance. Ici, la montre suisse tu oublieras...
    Un beau car décoré aux couleurs des Mokshû, et qui ne nous quittera plus, nous amène au Lorrain, lieu de résidence du groupe qui nous invite : <a href="https://www.facebook.com/BarylBandGroupeDeRue/" target="_blank">Le Baryl Band</a>.
    Ce village est situé tout au nord de l'île et c'est donc bien fatigués que nous pouvons faire connaissance avec les habitants chez qui nous logerons tout le long du séjour.
    Mais la journée n'est pas terminée car une super soirée de bienvenue a été organisée, le rhum coule à flot, le zouk à fond dans les baffles et c'est l'occasion des retrouvailles pour certains et de la découverte pour ceux qui n'étaient pas du voyage en 2001.
    Après une nuit agitée pour certains (la chaleur sûrement...), le soleil se lève sur les Antilles. Nous sommes au bord de la mer, entourés de cocotiers et de bananiers, il fait beau, il fait chaud, très chaud, quel dépaysement !
  </p>

  <h2>La Paradiz' Parade</h2>
  <p>
    Le groupe Baryl Band organise la parade du Nord, annonçant carnaval avec une semaine d'avance.
    Nos organismes ne sont pas encore habitués à cette chaleur et la lourdeur de nos costumes prévus pour l'hiver rend ce 1er cortège un peu difficile, voire même impossible à terminer pour quelques-uns.
    Un public nombreux et chaleureux nous redonnera du courage, mais ce n'est pas encore la folie carnavalesque !
  </p>

  <h2>En attendant carnaval, le vrai </h2>
  <p>Nous profitons de ces quelques jours pour découvrir l'île et ses magnifiques plages de sable blanc au Sud.</p>
  <p>
    Le mercredi, nous sommes invités à l'école du Lorrain pour leur donner un concert en lieu et place de leur cours de musique, les jeunes ont apprécié, normal...
    Le soir, nous participons à notre 1ère parade nocturne près de Fort-de-France avec un public très dense, on sent monter l'envie de fêter carnaval, le vrai.
    La fraîcheur relative de la nuit nous permet de mieux apprécier ce cortège et de régler nos chorégraphies pour les futures longues parades sous le soleil.
  </p>
  <p>
    Le lendemain, jeudi, nous avons rendez-vous avec le Conseil Général et le Conseil Régional à la capitale, nous y rencontrons des élus et des responsables touristiques et sommes invités dans les salles des parlements, impressionnant.
    Pendant tout notre voyage, notre porte-parole - Christian Baudoin - contacte quotidiennement notre radio Rhône FM pour un mini-reportage, c'est donc l'occasion d'entendre tous ces gens que nous croisons et de mettre aussi à contribution notre Conseiller Communal Marc-Henri Favre, "l'officiel" du voyage ! Merci encore à lui.
    Le soir, notre Titi fera découvrir à nos amis Martiniquais ce qu'est une torrée, superbe, bravo !
  </p>

  <h2>Les jours gras</h2>
  <p>
    C'est ainsi qu'ils appellent la période du vendredi au mercredi des cendres.
    C'est aussi pendant ces quelques jours que toute l'île va vivre uniquement pour son Carnaval !
    Une folie s'empare des Martiniquais qui vont fêter, danser, zouker, parader, se défouler et se travestir jusqu'au début du carême.
  </p>
  <p>
    Le vendredi, nous participons à une parade nocturne au Lamentin, le public est très nombreux et un peu indiscipliné.
    Nous apprendrons qu'il vaut mieux ne pas contrarier les scooters qui traversent le cortège à grande allure...
  </p>
  <p>
    Samedi, en fin d'après-midi, c'est une parade à Basse-Pointe avec une montée vertigineuse qui aura fait des dégâts dans les rangs.
    Un magnifique coucher de soleil nous accompagnera et c'est la seule fois où nous verrons la Montagne Pelée sans nuage.
    Un concert sur scène au coeur du village sera très apprécié du public.
    Ce sera d'ailleurs le seul vrai concert de tout le séjour.
  </p>
  <p>
    Dimanche, enfin, c'est la grande parade à Fort-de-France.
    Nous sommes prêts à affronter le soleil pendant près de 3 heures de musique et de danse non-stop.
    Les quelques 100'000 spectateurs, très enthousiastes à notre passage, nous donneront la force d'arriver au bout de ce magnifique défilé.
    Toutes les télévisions et radios sont en direct et notre porte-parole aura fort à faire pour satisfaire tous les animateurs.
    Même TF1 est là et nous promet quelques secondes au Journal de 13h (promesse tenue le vendredi 23.02... trop beau !)
    Ce cortège restera pour longtemps une référence pour notre guggen dans tous les sens du terme : tout simplement historique !
  </p>
  <p>
    Lundi, afin de nous remettre de nos émotions, le Baryl Band nous a organisé une grillade sur la plage avec les 2 groupes.
    Le temps est hélas un peu maussade, comme notre humeur, eh oui, demain c'est déjà le départ...
    Une soirée d'adieu complètement déjantée suivra, avec discours, cadeaux, quelques larmes déjà, mais surtout du rhum et du zouk.
    Les plus courageux enchaîneront avec le "vidé pyjama" à 5h00, afin de réveiller tout le village pour le mardi gras.
    Une petite heure de sommeil et il est déjà temps de démonter les instruments, les empaqueter et préparer les valises...
  </p>
  <p>
    Adieux à nos logeurs, émotion, on se sentait si bien chez eux... et en route pour l'aéroport.
    Mais il n'est pas encore l'heure du vrai départ, nous profitons d'aller voir la grande parade à la capitale.
    Pour une fois, c'est aussi agréable de n'être que spectateurs. Certains nous reconnaissent : alors les Suisses on fait la grêve ?
    Déjà des stars... zut on aurait dû rester un jour de plus.
  </p>
  <p>
    Le soir, tout le Baryl Band vient nous saluer à l'aéroport.
    Les trompettistes qui voyagent avec leur instrument entonnent une Ballade Nord-Irlandaise "de toute beauté" qui fera craquer les plus solides.
    Larmes, embrassades, promesses de se revoir bientôt et il est temps de prendre l'avion avec une boule au fond de la gorge et plein de soleil dans les yeux.
  </p>
  <p>Moment à vivre, moment unique.</p>
  <p>
    Amis Martiniquais, vous nous avez accueillis avec votre légendaire gentillesse et votre générosité.
    Ce voyage restera gravé à jamais dans le coeur de chaque participant.
    Les mots nous manquent, que vous dire d'autre qu'un immense MERCI.
  </p>
  <p>Alain, webmaster </p>

